<template>
  <div id="app">
    <router-view v-if="isRouterAlive"></router-view>
<!--    <span>移动订餐系统</span> &lt;!&ndash;    添加了这一行&ndash;&gt;-->
  </div>
</template>

<script>
export default {
  name: 'app',
  provide () {
    return {
      reload: this.reload
    }
  },
  data () {
    return {
      isRouterAlive: true
    }
  },
  components: {},
  methods: {
    reload () {
      this.isRouterAlive = false
      this.$nextTick(function () {
        this.isRouterAlive = true
      })
    }
  }
}
</script>

